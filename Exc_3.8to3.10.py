visit = ['Greece', 'China', 'Spain', 'Italy']

print(visit)

print(sorted(visit))

print(f"The original list: {visit}")

print(sorted(visit, reverse=True))

print(f"The original list: {visit}")

visit.reverse()
print(visit)

visit.reverse()
print(visit)

visit.sort()
print(f"In alphabetical format: {visit}")

visit.sort(reverse=True)
print(f"In reverse alphabetical format: {visit}")

